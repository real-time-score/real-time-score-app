  /// 아이콘 사이즈 프리셋
  static const double sizeXS = 12.0;
  static const double sizeSM = 16.0;
  static const double sizeMD = 20.0;
  static const double sizeLG = 24.0;
  static const double sizeXL = 28.0;
  static const double sizeXXL = 32.0;

  // ============================================
  // SVG HELPER METHODS
  // ============================================

  /// SVG 아이콘을 위젯으로 반환
  static Widget svg(
    String assetPath, {
    double? width,
    double? height,
    Color? color,
    BoxFit fit = BoxFit.contain,
  }) {
    return SvgPicture.asset(
      assetPath,
      width: width,
      height: height,
      colorFilter: color != null ? ColorFilter.mode(color, BlendMode.srcIn) : null,
      fit: fit,
    );
  }

  /// 미리 정의된 사이즈로 SVG 아이콘 반환
  static Widget svgWithSize(
    String assetPath,
    double size, {
    Color? color,
    BoxFit fit = BoxFit.contain,
  }) {
    return svg(assetPath, width: size, height: size, color: color, fit: fit);
  }

  /// XS 사이즈 아이콘 (12px)
  static Widget xs(String assetPath, {Color? color}) =>
      svgWithSize(assetPath, sizeXS, color: color);

  /// SM 사이즈 아이콘 (16px)
  static Widget sm(String assetPath, {Color? color}) =>
      svgWithSize(assetPath, sizeSM, color: color);

  /// MD 사이즈 아이콘 (20px) - 기본 사이즈
  static Widget md(String assetPath, {Color? color}) =>
      svgWithSize(assetPath, sizeMD, color: color);

  /// LG 사이즈 아이콘 (24px)
  static Widget lg(String assetPath, {Color? color}) =>
      svgWithSize(assetPath, sizeLG, color: color);

  /// XL 사이즈 아이콘 (28px)
  static Widget xl(String assetPath, {Color? color}) =>
      svgWithSize(assetPath, sizeXL, color: color);

  /// XXL 사이즈 아이콘 (32px)
  static Widget xxl(String assetPath, {Color? color}) =>
      svgWithSize(assetPath, sizeXXL, color: color);
}

